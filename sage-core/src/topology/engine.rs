//! DynamicTopologyEngine — central orchestrator for task-adaptive topology generation.
//!
//! Ties together S-MMU retrieval, MAP-Elites archive lookup, mutation, LLM synthesis,
//! and evolution for generating custom multi-agent DAGs per task.
//!
//! # 5-path strategy (priority order)
//!
//! 1. **S-MMU hit**: Retrieve similar past task from S-MMU. If similarity > 0.7 AND quality > 0.5,
//!    clone that topology. Inject bandit priors.
//! 2. **Archive hit**: Compute `BehaviorDescriptor` from task heuristics, look up MAP-Elites archive.
//!    If found AND quality > 0.5, use it.
//! 3. **LLM synthesis**: Skip in pure Rust (Python side calls synthesize() directly).
//! 4. **Mutation**: Pick best-by-quality from archive, apply random mutation.
//! 5. **Template fallback**: S1→sequential, S2→avr, S3→debate.

